class variables {
	public static void main(String args[]){
		// Declaring variables
		int myAge;
		String myName;
		boolean isTired;
		// Assigning values to variables
		myAge = 27;
		myName = "Max";
		isTired = false;

		// Can also assign a value at the same time the variable is declared.
		int notMyAge = 99;
		String notMyName = "Fred";
		boolean isAwake = true;
	}
}